        val rows = patternSet.trim().split("\n")
        rowsMap[index] = rows

        val cols = mutableListOf<String>()
        for (i in rows[0].indices) {
            val colBuilder = StringBuilder()
            for (row in rows) {
                colBuilder.append(row[i])
            }
            cols.add(colBuilder.toString())
        }
        colsMap[index] = cols
    }
    var horizontalCount = 0

    for ((key, value) in rowsMap) {

        for (i in 0..value.size - 2) {
            var isHorizontal = false
            if (value[i] == value[i + 1]) {
                isHorizontal = true
                var index1 = i - 1
                var index2 = i + 2
                while (index1 >= 0 && index2 < value.size) {
                    if (value[index1] != value[index2]) {
                        isHorizontal = false
                        break
                    }
                    index1--
                    index2++
                }
            }
            if (isHorizontal) horizontalCount += i + 1
        }
    }
    println("hor:$horizontalCount")
